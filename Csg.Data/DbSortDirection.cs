namespace Csg.Data;

/// <summary>
///     Represents the options of sort directions for a database query.
/// </summary>
public enum DbSortDirection
{
    /// <summary>
    ///     Unspecified sort direction
    /// </summary>
    None,

    /// <summary>
    ///     Sort ascending
    /// </summary>
    Ascending,

    /// <summary>
    ///     Sort descending
    /// </summary>
    Descending
}